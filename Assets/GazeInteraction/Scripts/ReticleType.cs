namespace TS.GazeInteraction
{
    /// <summary>
    /// Types of reticles (visual feedback) for gaze interactions.
    /// </summary>
    public enum ReticleType
    {
        /// <summary>
        /// Visible only on detection.
        /// </summary>
        Default,

        /// <summary>
        /// Always visible.
        /// </summary>
        Visible,

        /// <summary>
        /// Always invisible.
        /// </summary>
        Invisible
    }
}
